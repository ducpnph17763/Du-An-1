    private void FillTableHoaDon() {
        TaiKhoan tk = XAuth.user;
        List<Model.HoaDon> ls = hddao.SelectHoaDonNguoiDung(tk.getTenTK());
        
        DefaultTableModel mol = (DefaultTableModel)tblHoaDonKH.getModel();
        mol.setRowCount(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        for (Model.HoaDon l : ls) {
            Model.NhanVien nv = nvddao.selectById(l.getId_TC());
            mol.addRow(new Object[] {l.getId(), l.getId_KH(),l.getId_TC(), nv.getHoTen(),sdf.format(l.getNgayHen()), l.getThanhToan(), l.getDanhGia() });
        }
    }
